#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*cpy;
	size_t		i;

	i = ft_strlen(s1);
	if (!(cpy = malloc((i + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		cpy[i] = s1[i];
		++i;
	}
	cpy[i] = s1[i];
	return (cpy);
}
